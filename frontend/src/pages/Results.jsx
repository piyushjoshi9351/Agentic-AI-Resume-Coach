import React, { useEffect, useState } from 'react'
import { Link, useLocation, useNavigate } from 'react-router-dom'
import ResultsTabs from '../components/ResultsTabs'
import { downloadReport } from '../services/api'
import { getLatestAnalysis, setLatestAnalysis } from '../lib/storage'

export default function Results() {
  const navigate = useNavigate()
  const location = useLocation()
  const [results, setResults] = useState(null)

  useEffect(() => {
    const routeResults = location.state?.results
    const storedResults = getLatestAnalysis()
    const nextResults = routeResults || storedResults

    if (nextResults) {
      setResults(nextResults)
      setLatestAnalysis(nextResults)
    }
  }, [location.state])

  const handleDownload = async () => {
    if (!results?.analysis_id) return
    const blob = await downloadReport(results.analysis_id)
    const url = window.URL.createObjectURL(blob)
    const anchor = document.createElement('a')
    anchor.href = url
    anchor.download = `analysis-report-${results.analysis_id}.pdf`
    document.body.appendChild(anchor)
    anchor.click()
    anchor.remove()
    window.URL.revokeObjectURL(url)
  }

  if (!results) {
    return (
      <div className="rounded-[2rem] border border-slate-800 bg-slate-900/70 p-8 text-center shadow-2xl shadow-black/20">
        <h1 className="text-2xl font-bold text-white">No analysis loaded yet</h1>
        <p className="mt-3 text-sm text-slate-300">Run a live analysis first to view the results page.</p>
        <Link to="/analyze" className="mt-6 inline-flex rounded-2xl bg-gradient-to-r from-purple-500 to-blue-500 px-5 py-3 text-sm font-semibold text-white">
          Go to Analyze
        </Link>
      </div>
    )
  }

  return (
    <div className="space-y-8">
      <section className="rounded-[2rem] border border-slate-800 bg-slate-900/70 p-6 shadow-2xl shadow-black/20 backdrop-blur-xl">
        <div className="flex flex-col gap-4 lg:flex-row lg:items-end lg:justify-between">
          <div>
            <p className="text-xs uppercase tracking-[0.28em] text-slate-400">Results</p>
            <h1 className="mt-2 text-3xl font-bold text-white">Your live analysis output</h1>
            <p className="mt-2 max-w-2xl text-sm leading-7 text-slate-300">
              Review the ATS score, resume insights, cover letter, and interview questions generated by the backend.
            </p>
          </div>

          <div className="flex flex-wrap gap-3">
            <button
              type="button"
              onClick={handleDownload}
              disabled={!results.analysis_id}
              className="rounded-2xl border border-slate-700 bg-slate-900/70 px-5 py-3 text-sm font-semibold text-slate-200 disabled:opacity-50"
            >
              Download Report
            </button>
            <button
              type="button"
              onClick={() => navigate('/interview')}
              className="rounded-2xl bg-gradient-to-r from-purple-500 to-blue-500 px-5 py-3 text-sm font-semibold text-white"
            >
              Start Interview Practice
            </button>
          </div>
        </div>

        <div className="mt-6 rounded-2xl border border-slate-800 bg-slate-950/60 p-4 text-sm text-slate-300">
          Analysis ID: {results.analysis_id || 'Not saved'}
        </div>
      </section>

      <ResultsTabs results={results} />

      <div className="flex flex-wrap gap-3">
        <Link to="/analyze" className="rounded-2xl border border-slate-700 bg-slate-900/70 px-5 py-3 text-sm font-semibold text-slate-200">
          Back to Analyze
        </Link>
        <Link to="/tools" className="rounded-2xl border border-purple-500/30 bg-purple-500/10 px-5 py-3 text-sm font-semibold text-purple-200">
          Open Tools
        </Link>
      </div>
    </div>
  )
}